package com.mycompany.ex_aula_java;
public class Ex_aula_java {

    public static void main(String[] args) {
        System.out.println("Numeros combinados 1 a 4");
        for (int i = 1; i <= 4; i++) {
            for (int j = 1; j <= 4; j++) {
                System.out.println(i + " com " + j);
            }
        }
    }
}
